import 'dart:io';
import 'dart:math';

// 1. Write a dart program to check if the number is odd or even.
void checkOddEven(int number) {
  if (number % 2 == 0) {
    print('$number is Even.');
  } else {
    print('$number is Odd.');
  }
}
